public class IntegerLiteral {
    public static void main(String[] arg) {

        // 1. Decimal

        int x = 9999;
        System.out.println("Decimal: "+x);

        // 2. Octal
        int y = 0011;
        System.out.println("Octal: "+y);

        // 3. Hexadecimal
        byte z = 0x0011A;

        System.out.println("Hexadecimal: "+z);

    }
}